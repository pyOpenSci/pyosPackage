"""A module that performs basic math operations."""


def add_num(
    a: int,
    b: int,
) -> int:
    """A function that adds two  numbers together

    Parameters
    ----------
    a : int
        The first integer to be added.
    b : int
        The second integer to be added.

    Returns
    -------
    int
        The sum of the two provided integers.
    """
    return a + b
